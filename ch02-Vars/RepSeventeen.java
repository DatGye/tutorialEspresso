class RepSeventeen {
	
	public static void main (String[] args) {
		
		System.out.println();
		System.out.println("The integer value 17 represents " + 17);
		System.out.println("The long value 17L represents " + 17L);
		System.out.println("The float value 17F represents " + 17F);
		System.out.println("The decimal value 17D represents " + 17D);
		System.out.println();
		System.out.println("The hex value 0x11 represents " + 0x11);
		System.out.println("The octal value 021 represents " + 021);
		System.out.println("The bin value 0b10001 represents " + 0b10001);
		System.out.println();
		System.out.println("The string value \"17\" represents " + "17");

	}
	
}

/*
	The integer value 17 represents 17
	The long value 17L represents 17
	The float value 17F represents 17.0
	The decimal value 17D represents 17.0

	The hex value 0x11 represents 17
	The octal value 021 represents 17
	The bin value 0b10001 represents 17

	The string value "17" represents 17
*/